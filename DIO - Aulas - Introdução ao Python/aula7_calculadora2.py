class calculadora:
    def __init__(self,valor_a,valor_b):
        pass
    def soma(valor_a,valor_b):
        return valor_a + valor_b
    def subtracao(valor_a,valor_b):
        return valor_a - valor_b
    def multiplicacao(valor_a,valor_b) -> object:
        return  valor_a * valor_b
    def divisao(valor_a,valor_b):
        return valor_a / valor_b


print(calculadora.soma(10,5,))
print(calculadora.subtracao(20,6))
print(calculadora.multiplicacao(30,3))
print(calculadora.divisao(100,50))